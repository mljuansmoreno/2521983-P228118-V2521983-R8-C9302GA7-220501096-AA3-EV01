package tu.asesoria.financiera;


// Importa las clases necesarias para los campos y métodos de la clase Post

public class Post {

    // Atributos de la clase Post

    private String title; // Título del post
    private String content; // Contenido del post

    // Genera los getters y setters para los atributos

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
